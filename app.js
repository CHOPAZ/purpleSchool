// assignment and comparison operators

let age = 18 + 5; // assignment operator
age += 2 // age = age + 2
age -= 3 // age = age - 3
age *= 2 // age = age * 2
age /= 2 // age = age / 2

age++; // age = age + 1
age--; // age = age - 1
console.log(age);

// Operators comparison
const vasia = 20;
console.log(age > vasia);
console.log(age >= vasia);
console.log(age < vasia);
console.log(age <= vasia);
console.log(age == vasia);